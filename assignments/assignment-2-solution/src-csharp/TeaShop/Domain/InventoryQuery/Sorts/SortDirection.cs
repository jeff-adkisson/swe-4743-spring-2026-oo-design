namespace Assignment2Solution.Domain.InventoryQuery.Sorts;

/// <summary>
///     Specifies the direction of a sort operation.
/// </summary>
public enum SortDirection
{
    /// <summary>
    ///     Sort from smallest to largest.
    /// </summary>
    Ascending,

    /// <summary>
    ///     Sort from largest to smallest.
    /// </summary>
    Descending
}